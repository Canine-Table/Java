/**
 * Wrapper class = provides a way of using primitive data types as reference data tyoes
 * 				   reference data types contain useful methods
 * 				   can be used with collections like arrays, hashtables, hashmaps, etc.
 *
 * 		primitive 		wrapper
 * 									
 * 		boolean			Boolean
 * 		char			Character
 * 		int				Integer
 * 		double			Double
 * 
 * 	autoboxing = the automatic conversion that the Java compiler makes between the primitive classes 
 * 		and their corresponding wrapper classes.
 * 	unboxing = the reverse of autoboxing Automatic conversion of wrapper classes to primitive classes.
 */
package levelOne;
/**
 * @author Table
 *
 */
